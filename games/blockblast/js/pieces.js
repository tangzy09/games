// ════════════════════════════════════════
// pieces.js — 拼块表（DESIGN §1.1）。不可旋转 ⇒ 每个朝向是一个独立的块。
// 权重是**固定的**：不看棋盘、不看玩家（DESIGN §2 的公平承诺之一）。
// 权重表原样印在游戏内「公平」页里，所以这里的数字就是对玩家的公开承诺。
// ════════════════════════════════════════
(function (root) {
  'use strict';

  // cells: [row, col] 相对坐标，(0,0) 为 bounding box 左上
  const def = (id, cls, w, cells) => ({ id, cls, w, cells, size: cells.length,
    h: Math.max(...cells.map(c => c[0])) + 1, wdt: Math.max(...cells.map(c => c[1])) + 1 });

  const PIECES = [
    // ── 小档：合计 25 ──
    def('i1',   'S', 4,    [[0,0]]),
    def('i2h',  'S', 4,    [[0,0],[0,1]]),
    def('i2v',  'S', 4,    [[0,0],[1,0]]),
    def('i3h',  'S', 3,    [[0,0],[0,1],[0,2]]),
    def('i3v',  'S', 3,    [[0,0],[1,0],[2,0]]),
    def('l3a',  'S', 1.75, [[0,0],[1,0],[1,1]]),
    def('l3b',  'S', 1.75, [[0,0],[0,1],[1,0]]),
    def('l3c',  'S', 1.75, [[0,0],[0,1],[1,1]]),
    def('l3d',  'S', 1.75, [[0,1],[1,0],[1,1]]),

    // ── 中档：合计 55 ──
    def('o4',   'M', 6,    [[0,0],[0,1],[1,0],[1,1]]),
    def('i4h',  'M', 5,    [[0,0],[0,1],[0,2],[0,3]]),
    def('i4v',  'M', 5,    [[0,0],[1,0],[2,0],[3,0]]),
    // L 四向
    def('l4a',  'M', 2.5,  [[0,0],[1,0],[2,0],[2,1]]),
    def('l4b',  'M', 2.5,  [[0,0],[0,1],[0,2],[1,0]]),
    def('l4c',  'M', 2.5,  [[0,0],[0,1],[1,1],[2,1]]),
    def('l4d',  'M', 2.5,  [[0,2],[1,0],[1,1],[1,2]]),
    // J 四向（L 的镜像）
    def('j4a',  'M', 2.5,  [[0,1],[1,1],[2,0],[2,1]]),
    def('j4b',  'M', 2.5,  [[0,0],[1,0],[1,1],[1,2]]),
    def('j4c',  'M', 2.5,  [[0,0],[0,1],[1,0],[2,0]]),
    def('j4d',  'M', 2.5,  [[0,0],[0,1],[0,2],[1,2]]),
    // S / Z 各两向
    def('s4h',  'M', 2.5,  [[0,1],[0,2],[1,0],[1,1]]),
    def('s4v',  'M', 2.5,  [[0,0],[1,0],[1,1],[2,1]]),
    def('z4h',  'M', 2.5,  [[0,0],[0,1],[1,1],[1,2]]),
    def('z4v',  'M', 2.5,  [[0,1],[1,0],[1,1],[2,0]]),
    // T 四向
    def('t4a',  'M', 2.25, [[0,0],[0,1],[0,2],[1,1]]),
    def('t4b',  'M', 2.25, [[0,1],[1,0],[1,1],[1,2]]),
    def('t4c',  'M', 2.25, [[0,0],[1,0],[1,1],[2,0]]),
    def('t4d',  'M', 2.25, [[0,1],[1,0],[1,1],[2,1]]),

    // ── 大档：合计 20 ──
    def('i5h',  'L', 4,    [[0,0],[0,1],[0,2],[0,3],[0,4]]),
    def('i5v',  'L', 4,    [[0,0],[1,0],[2,0],[3,0],[4,0]]),
    def('plus', 'L', 3,    [[0,1],[1,0],[1,1],[1,2],[2,1]]),
    def('r23',  'L', 3,    [[0,0],[0,1],[0,2],[1,0],[1,1],[1,2]]),
    def('r32',  'L', 3,    [[0,0],[0,1],[1,0],[1,1],[2,0],[2,1]]),
    def('sq3',  'L', 3,    [[0,0],[0,1],[0,2],[1,0],[1,1],[1,2],[2,0],[2,1],[2,2]]),
  ];

  const TOTAL_W = PIECES.reduce((a, p) => a + p.w, 0);   // = 100
  const byId = id => PIECES.find(p => p.id === id);

  // 加权抽取：给一个 [0,1) 的随机数，返回块。**只依赖这个数**，别无输入。
  function pick(u) {
    let x = u * TOTAL_W;
    for (const p of PIECES) { x -= p.w; if (x < 0) return p; }
    return PIECES[PIECES.length - 1];
  }

  const API = { PIECES, TOTAL_W, byId, pick };
  if (typeof module !== 'undefined' && module.exports) module.exports = API;
  else root.Pieces = API;
})(typeof self !== 'undefined' ? self : this);
